//replicatePeanoBegin
let rec replicatePeano (x: 'a) (n: Nat): 'a list =
    if n = 1N then [x]
    else x :: replicatePeano x (n - 1N)
//replicatePeanoEnd


//replicateLeibnizBegin
let rec replicateLeibniz (x: 'a) (n: Nat): 'a list =
    if n = 0N then []
    else
        let xs: 'a list = replicateLeibniz x (n / 2N)
        if n % 2N = 0N then x :: (xs @ xs)
        else x :: x :: (xs @ xs)
//replicateLeibnizEnd


