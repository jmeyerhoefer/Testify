//replicatePeanoBegin
let rec replicatePeano (x: 'a) (n: Nat): 'a list =
    if n = 0N then []
    else x :: replicatePeano x (n - 1N)
//replicatePeanoEnd


//replicateLeibnizBegin
let rec replicateLeibniz (x: 'a) (n: Nat): 'a list =
    if n = 0N then []
    else
        let xs: 'a list = replicateLeibniz x (n / 2N)
        if n % 2N = 0N then xs @ xs
        else x :: (xs @ xs)
//replicateLeibnizEnd


//replicateBegin
let replicate (x: 'a) (n: Nat): 'a list =
    List.replicate (int n) x
//replicateEnd


