//filterBegin
let rec filter (pred: Nat -> bool) (xs: Nats): Nats =
    match xs with
    | Nil -> Nil
    | Cons (y, ys) ->
        if pred y then Cons (y, filter pred ys)
        else filter pred ys
//filterEnd


